    if love.filesystem.getInfo(archiveName) then
        love.filesystem.remove(archiveName)
        log.info("Overwriting: " .. archiveName)
    end
    
    -- SFBファイル生成
    local header = "SFB1\n1\n" .. #files .. "\n"
