        local parts = {}
        for k, v in pairs(t) do
            local key = (type(k) == "string") and string.format("[%q]", k) or string.format("[%d]", k)
            table.insert(parts, key .. "=" .. serializeValue(v))
        end
        return "{" .. table.concat(parts, ",") .. "}"
    end
    
    local chartData = "return " .. basicSerialize(chartTable)
    
